    private void mostrarDatos(String valorBuscar) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID_lector");
        modelo.addColumn("Nombre");
        modelo.addColumn("Direccion");
        modelo.addColumn("Telefono");
        modelo.addColumn("Ciudad");
        modelo.addColumn("Correo");

        tblEmpleados.setModel(modelo);
        String consultaSQL = "SELECT * FROM Lectores WHERE Nombre LIKE '%" + valorBuscar + "%'";
        //String consultaSQL = "SELECT * FROM empleadoss_departamentoss.empleados;;
        String data[] = new String[10];
        Statement st;
        try {
            st = (Statement) cn.createStatement();
            ResultSet rs = st.executeQuery(consultaSQL);
            while (rs.next()) {
                data[0] = rs.getString(1);
                data[1] = rs.getString(2);
                data[2] = rs.getString(3);
                data[3] = rs.getString(4);
                data[4] = rs.getString(5);
                data[5] = rs.getString(6);
                modelo.addRow(data);
            }
            tblEmpleados.setModel(modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error en la consulta SQL: " + ex);
        }
    }

