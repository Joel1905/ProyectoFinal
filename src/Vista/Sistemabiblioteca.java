    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
        // TODO add your handling code here:
        String actualizarSQL = "UPDATE Lectores SET Nombre='" + txtnombre.getText()
        + "', Direccion='" + txtdireccion.getText()
        + "', Telefono='" + txttelefono.getText()
        + "', Ciudad='" + txtciudad.getText()
        + "', Correo='" + txtcorreo.getText()
        + "' WHERE Id_lector ='" + txtlector.getText()+"';";
        try {
            PreparedStatement ps;
            ps = (PreparedStatement) cn.prepareStatement(actualizarSQL);
            int indice = ps.executeUpdate();
            if (indice > 0) {
                JOptionPane.showMessageDialog(rootPane, "Datos actualizados correctamente.");
                btnactualizar.setEnabled(false);
                btneliminar.setEnabled(false);
                btnagregar.setEnabled(true);
                mostrarDatos("");
                limpiarCampos();
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al actualizar datos: " + ex);
        }
    }//GEN-LAST:event_btnactualizarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(rootPane, "Se eliminara el registro, desea continuar?", "Eliminar Registro",
            JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
        try {
            String Borrar = "DELETE FROM Lectores WHERE ID_Lector='" + txtlector.getText() + "';";
            PreparedStatement ps;
            ps = (PreparedStatement) cn.prepareStatement(Borrar);
            int respuesta = ps.executeUpdate();
            if (respuesta > 0) {
                JOptionPane.showMessageDialog(null, "Registro eliminado con exito!");
                limpiarCampos();
                mostrarDatos("");
            } else {
                JOptionPane.showMessageDialog(null, "No ha seleccionado la fila a ser borrada.");
                limpiarCampos();
                mostrarDatos("");
            }
        } catch (SQLException e) {
            System.err.println("Error al eliminar...." + e);
            JOptionPane.showMessageDialog(null, "Error al eliminar!" + e);
        }

        } else {
            limpiarCampos();
            mostrarDatos("");
            btnactualizar.setEnabled(false);
            btneliminar.setEnabled(false);
            btnagregar.setEnabled(true);
        }
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed

        String insertarSQL = "INSERT INTO `Lectores` (Nombre, Direccion, Telefono, Ciudad, Correo) VALUES (?,?,?,?,?);";
        try {
            PreparedStatement cs;
            cs = (PreparedStatement) cn.prepareStatement(insertarSQL);
            cs.setString(1, txtnombre.getText());
            cs.setString(2, txtdireccion.getText());
            cs.setString(3, txttelefono.getText());
            cs.setString(4, txtciudad.getText());
            cs.setString(5, txtcorreo.getText());
            cs.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Registro realizado con exito.");
            btnactualizar.setEnabled(false);
            btneliminar.setEnabled(false);
            btnagregar.setEnabled(true);
            mostrarDatos("");
            limpiarCampos();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al tratar de insertar los datos: " + ex);
        }
    }//GEN-LAST:event_btnagregarActionPerformed

